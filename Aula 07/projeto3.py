num = int(input("Informe um número: "))
if num % 2 == 0:
    if num > 0:
        print("Esse número é par e positivo")
    else:
        print("Esse número é par e positivo")
else:
    if num > 0:
        print("Esse número é impar e positivo")
    else:
        print("Esse número é impar e negativo")